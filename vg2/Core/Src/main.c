#define ADC_SAMPLES 10  // Number of samples for averaging
uint16_t adc_buffer[ADC_SAMPLES] = {0};  // Store previous readings
uint8_t adc_index = 0;  // Index for the circular buffer

double debounce_adc_reading(uint16_t new_reading) {
 // Store new reading
 adc_buffer[adc_index] = new_reading;
 adc_index = (adc_index + 1) % ADC_SAMPLES;

// Calculate the average
 uint32_t sum = 0;
 for (uint8_t i = 0; i < ADC_SAMPLES; i++) {
	sum += adc_buffer[i];
 }
 return ((double)sum / ADC_SAMPLES);
}

